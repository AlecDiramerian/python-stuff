import random
while True:
    num = random.randint(1, 10000000)
    print(num)
    if num == 69:
        print(num)
        exit()
    elif num == 420:
        print(num)
        exit()
    elif num == 1:
        print(num)
        exit()
    elif num == 10000000:
        print(num)
        exit()